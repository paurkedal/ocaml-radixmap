open Printf

let init test_name =
  let seed =
    try int_of_string (Unix.getenv "SEED")
    with Not_found -> int_of_float (Unix.time ()) in
  Random.init seed;
  printf "Running %s with seed %d (keep if you hit a bug).\n"
         test_name seed
